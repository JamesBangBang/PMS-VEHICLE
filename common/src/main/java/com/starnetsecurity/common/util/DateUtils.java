    /**
     * 将日期字符串转化为日期。失败返回null。
     *
     * @param date     日期字符串
     * @param parttern 日期格式
     * @return 日期
     */
    public static Date StringToDate(String date, String parttern) {
        Date myDate = null;
        if (date != null) {
            try {
                myDate = getDateFormat(parttern).parse(date);
            } catch (Exception e) {
            }
        }else {
            try {
                myDate = getDateFormat("yyyy-MM-dd HH:mm:ss").parse(date);

            } catch (Exception ex) {

            }
        }
        return myDate;
    }


